# Define the function that compares two numbers
def greaterThan(x, y):
    return x > y

# First test case
a = 2
b = 3
result = greaterThan(a, b)
print("The statement " + str(a) + " is greater than " + str(b) + " is " + str(result))

# Second test case
a = 10
b = 6
result = greaterThan(a, b)
print("The statement " + str(a) + " is greater than " + str(b) + " is " + str(result))
